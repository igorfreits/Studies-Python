# Tipos de dados

# str = string - textos dentro de " " ou ' '
# "Texto"

# int = numero inteiro
# -123, 456

# float = numero real/ponto flutuante
# 1.5, -10.6

# bool = booleans/logico
# True/False - comparação de valor - 0 ou coisas vazias são considerados False

print('Igor', type('Igor'))  # nome
print(23, type(23))  # idade
print(1.75, type(1.75))  # altura
print(23 > 18, type(23 > 10))  # maior de idade
