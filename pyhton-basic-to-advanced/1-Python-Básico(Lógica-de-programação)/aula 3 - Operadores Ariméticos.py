# operadores ariméticos

# + = adição
# - = subtração
# * = multiplicação
# / = divisão
# // = divisão por int
# ** = potencia
# % = resto da divisão
# () = alterar ordem da precedência

# ordem de precedência básica = () , **, /, //, %, +, -

print('Multiplicação * = ', 10 * 10)
print('Repetição * com str = ', 10 * 'Laranja')

print('Adição + = ', 10 + 10)
print('Concatenação/juntar str + str = ', '10' + '50')
print('Igor' + ' ' + 'Freitas' + ' tem ' + str(23) + ' anos')

print('Subtração -', 10 - 5)
print('Divisão /', 10 / 2)

print('Divisão por Int // = ', 10.5 // 3)
print('Potência ** = ', 2 ** 10)
print('Retorna o resto da divisão % = ', 10 % 3)
print('Precedência () = ', (5 + 2)*10)
