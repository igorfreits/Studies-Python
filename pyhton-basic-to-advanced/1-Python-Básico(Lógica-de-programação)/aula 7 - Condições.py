"""
Condições IF, ELIF, e ELSE
"""

if False:  # Se
    print('Verdadeiro')  # so sera executado se a condição foi verdadeira
elif False:  # Ou se
    print('Agora é verdadeiro')
elif False:  # so sera executado se o "if" for falso
    print('Mais uma verdadeira')
else:  # Se não
    # so sera mostrado se a condição for Falsa
    print('A minha expressão nao é verdadeira')
